# mors kodu cümleye çeviriliyor.
coded = input("lütfen mors kodunu giriniz: ").split()
decoded = ""
for i in coded:
    for j in morse_codes.keys():
        if i == morse_codes[j]:
            decoded += j+" "
print(decoded)    